//需求：打印5次HollWorld
package Day04.fordem;

public class forDemo01 {
    public static void main(String[] args) {
        // for循环格式:
        /*
         * for(初始化语句;条件判断语句;条件控制语句)
         * 循环体语句;
         * }
         */
        for (int i = 1; i <= 10000000; i++) {// i = 1;表示变量从1开始的，i <=5表示到5结束了，i++自曾一次
            System.out.println("Holl,World!!");
        }

    }
}
