package Day06.method;

public class MethodDemo01 {
  public static void main(String[] args) {
    // 调用方法
    playGema();
  }

  // 注意 方法要写在main方法的外面，类的里面
  // 定义一方法
  // 方法定义的格式
  // public static void 方法名(){
  // 方法体；
  // }

  // 调用格式
  // 方法名();

  public static void playGema() {
    System.out.print("h");
    System.out.print("e");
    System.out.print("l");
    System.out.print("l");
    System.out.print("o");
    System.out.println("   ");
    System.out.print("w");
    System.out.print("o");
    System.out.print("r");
    System.out.print("l");
    System.out.print("d");
  }
}